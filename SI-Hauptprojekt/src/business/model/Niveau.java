package business.model;

public class Niveau {
	
	private int idNiveau;
	private String name;
	
	public void setIdNiveau(int idNiveau) {
		this.idNiveau = idNiveau;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdNiveau() {
		return idNiveau;
	}
	public String getName() {
		return name;
	}
	

}
