package business.model;

public class Termin {
	
	private int idTermin;
	private int idKurs;
	private int idWochentag;
	private int anfangszeit;
	private int endzeit;
	
	public int getIdTermin() {
		return idTermin;
	}
	public int getIdKurs() {
		return idKurs;
	}
	public int getIdWochentag() {
		return idWochentag;
	}
	public int getAnfangszeit() {
		return anfangszeit;
	}
	public int getEndzeit() {
		return endzeit;
	}

}
