package business.model;

public class Campus {
	private int idCampus;
	private String Name;

	public int getIdCampus() {
		return idCampus;
	}

	public String getName() {
		return Name;
	}

	public void setIdCampus(int idCampus) {
		this.idCampus = idCampus;
	}

	public void setName(String name) {
		Name = name;
	}
}
